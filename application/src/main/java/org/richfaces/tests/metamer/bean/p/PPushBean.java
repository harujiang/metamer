package org.richfaces.tests.metamer.bean.p;

import java.io.Serializable;
import java.util.Date;

import javax.annotation.PostConstruct;
import javax.faces.bean.ManagedBean;
import javax.faces.bean.SessionScoped;

import org.primefaces.comet.CometContext;
import org.richfaces.tests.metamer.Message;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 * Managed bean for p:push.
 *
 * @author <a href="mailto:ppitonak@redhat.com">Pavol Pitonak</a>
 * @version $Revision$
 */
@ManagedBean(name = "pPushBean")
@SessionScoped
public class PPushBean implements Serializable {

    private static Logger logger;
    private String username;
    private String message;

    /**
     * Initializes the managed bean.
     */
    @PostConstruct
    public void init() {
        logger = LoggerFactory.getLogger(getClass());
        logger.debug("initializing bean " + getClass().getName());
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public void send() {
        logger.info("sending message \"" + message + "\" by user " + username);
        Message msg = new Message(message, username, new Date().toString());
        CometContext.publish("metamer", msg);
    }
}
